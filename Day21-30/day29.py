# Day - 29 Docstrings & PEP-8 in Python 

# Docstrings: strings literals that appear 'right after' the definition 
# of a function, method, class or module.
def square(n):
    '''Take in a number n, returns the square of n'''  #this is a docstring
    print(n**2)

square(5)
print(square.__doc__)  #docstring


